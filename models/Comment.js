// *** Dependencies
// =============================================================
var mongoose = require('mongoose');

// =============================================================
// *** Create Schema
// =============================================================
var Schema = mongoose.Schema;

var ArticleComment = new Schema ({
	text: {
		type: String,
		minlength: 1
	},
	createdAt: {
    type: Date,
    default: Date.now
  	}
});


// =============================================================
// *** Create Comment Model
// =============================================================
var Comment = mongoose.model('Comment', ArticleComment);

// =============================================================
// *** Export the Comment Model
// =============================================================
module.exports = Comment;